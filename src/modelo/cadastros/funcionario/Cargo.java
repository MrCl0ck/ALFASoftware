package modelo.cadastros.funcionario;

public class Cargo {

	private int id;
	private String nomeCargo;
	
	public Cargo() {
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNomeCargo() {
		return nomeCargo;
	}

	public void setNomeCargo(String nomeCargo) {
		this.nomeCargo = nomeCargo;
	}
}
