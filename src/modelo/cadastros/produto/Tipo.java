package modelo.cadastros.produto;

public class Tipo {
	
	private int id;
	private String tipoNome;
	
	public Tipo() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTipoNome() {
		return tipoNome;
	}

	public void setTipoNome(String tipoNome) {
		this.tipoNome = tipoNome;
	}

}
